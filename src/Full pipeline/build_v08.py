    "def train_model(session_list, verbose=True, gbm_params=None):\n",
    "    \"\"\"Enhanced training: min-cycle filter + shape PCA.\"\"\"\n",
    "    from sklearn.decomposition import PCA as _PCA\n",
    "    fs = CONFIG['FS']\n",
    "    all_sessions, all_labels, all_dnames = [], [], []\n",
    "    skipped = 0\n",
    "\n",
    "    for entry in session_list:\n",
    "        d0, d1, label, dname, tw = entry\n",
    "        sess = _load_entry(entry)\n",
    "        n_cycles = len(sess['paired0'])\n",
    "        if n_cycles < MIN_CYCLES_PER_ENTRY:\n",
    "            if verbose:\n",
    "                print(f'  SKIP {label:<22s} [{dname}]: {n_cycles} cyc (< {MIN_CYCLES_PER_ENTRY})')\n",
    "            skipped += 1\n",
    "            continue\n",
    "        all_sessions.append(sess)\n",
    "        all_labels.append(label)\n",
    "        all_dnames.append(dname)\n",
    "        if verbose:\n",
    "            print(f'  {label:<22s} [{dname}]: {n_cycles} cycles')\n",
    "\n",
    "    if verbose and skipped:\n",
    "        print(f'  ({skipped} entries skipped)')\n",
    "\n",
    "    all_cms = []\n",
    "    for sess in all_sessions:\n",
    "        all_cms.extend(sess['cycle_matrices'])\n",
    "    if not all_cms:\n",
    "        raise ValueError('No cycles found.')\n",
    "\n",
    "    template = build_template(all_cms)\n",
    "    class_cms = defaultdict(list)\n",
    "    for sess, lbl in zip(all_sessions, all_labels):\n",
    "        class_cms[lbl].extend(sess['cycle_matrices'])\n",
    "    class_templates = {lbl: build_template(cms) for lbl, cms in class_cms.items()}\n",
    "\n",
    "    # Extract features (without PCA first)\n",
    "    X_list, flat_list, y_list, sid_list = [], [], [], []\n",
    "    for si, (sess, lbl) in enumerate(zip(all_sessions, all_labels)):\n",
    "        X, flat = extract_features(\n",
    "            sess['t0'], sess['t1'], sess['A0'], sess['A1'],\n",
    "            sess['om0'], sess['om1'], sess['paired0'], sess['paired1'],\n",
    "            template, fs, shape_pca=None,\n",
    "            selected_base_idx=SELECTED_BASE_IDX)\n",
    "        X = np.nan_to_num(X, nan=0.0, posinf=0.0, neginf=0.0)\n",
    "        X_list.append(X)\n",
    "        flat_list.append(flat)\n",
    "        y_list.extend([lbl] * len(X))\n",
    "        sid_list.extend([si] * len(X))\n",
    "\n",
    "    X_base = np.vstack(X_list)\n",
    "    flat_all = np.vstack(flat_list) if flat_list else np.empty((0, 768))\n",
    "    y_all = np.array(y_list)\n",
    "    sid_all = np.array(sid_list)\n",
    "\n",
    "    # Fit shape PCA and keep top-k PCs capturing target variance\n",
    "    pca_cap = min(max(1, flat_all.shape[0] - 1), flat_all.shape[1])\n",
    "    shape_pca = _PCA(n_components=pca_cap, svd_solver='full')\n",
    "    pca_scores = shape_pca.fit_transform(flat_all)\n",
    "    cumvar = np.cumsum(shape_pca.explained_variance_ratio_)\n",
    "    n_use = int(np.searchsorted(cumvar, SHAPE_PCA_VAR_TARGET) + 1)\n",
    "    n_use = max(1, min(n_use, pca_scores.shape[1]))\n",
    "    achieved_cumvar = float(cumvar[n_use - 1])\n",
    "    pca_keep_idx = [i for i in STABLE_PCA_COMPONENTS if i < n_use]\n",
    "    if not pca_keep_idx:\n",
    "        pca_keep_idx = list(range(n_use))\n",
    "    pca_scores_keep = pca_scores[:, pca_keep_idx]\n",
    "    X_all = np.hstack([X_base, pca_scores_keep])\n",
    "    feature_names = BASE_FEAT_NAMES + [f'{SHAPE_PCA_NAME_PREFIX}{i}' for i in pca_keep_idx]\n",
    "\n",
    "    # Per-session normalization to reduce subject/session scale shifts\n",
    "    for si in np.unique(sid_all):\n",
    "        mask = (sid_all == si)\n",
    "        Xi = X_all[mask]\n",
    "        mu = np.mean(Xi, axis=0, keepdims=True)\n",
    "        sd = np.std(Xi, axis=0, keepdims=True)\n",
    "        X_all[mask] = (Xi - mu) / (sd + 1e-6)\n",
    "\n",
    "    scaler = StandardScaler()\n",
    "    X_s = scaler.fit_transform(X_all)\n",
    "    le = LabelEncoder()\n",
    "    y_enc = le.fit_transform(y_all)\n",
    "\n",
    "    class_counts = Counter(y_all.tolist())\n",
    "    class_weights = {\n",
    "        cls: len(y_all) / (len(class_counts) * cnt)\n",
    "        for cls, cnt in class_counts.items()\n",
    "    }\n",
    "    sample_weight = np.array([class_weights[y] for y in y_all], dtype=float)\n",
    "\n",
    "    params = gbm_params or {'n_estimators': 350, 'max_depth': 4,\n",
    "                             'learning_rate': 0.05}\n",
    "    clf = GradientBoostingClassifier(**params, random_state=42)\n",
    "    clf.fit(X_s, y_enc, sample_weight=sample_weight)\n",
    "\n",
    "    if verbose:\n",
    "        print(f'\\nTrained on {X_all.shape[0]} cycles x {X_all.shape[1]} features')\n",
    "        print(f'Classes: {list(le.classes_)}')\n",
    "        print('Class distribution (cycles):')\n",
    "        for cls, cnt in sorted(class_counts.items()):\n",
    "            print(f'  {cls:<22s}: {cnt:>5d} (w={class_weights[cls]:.3f})')\n",
    "        print(f'Shape PCA components needed for target variance: {n_use} (target variance={SHAPE_PCA_VAR_TARGET:.2f})')\n",
    "        print(f'Shape PCA components kept after stability pruning: {len(pca_keep_idx)} -> {pca_keep_idx}')\n",
    "        print(f'Achieved cumulative variance at n_use: {achieved_cumvar:.4f}')\n",
    "\n",
    "    return {\n",
    "        'clf': clf, 'scaler': scaler, 'label_encoder': le,\n",
    "        'template': template, 'class_templates': class_templates,\n",
    "        'shape_pca': shape_pca,\n",
    "        'shape_pca_n_components': len(pca_keep_idx),\n",
    "        'shape_pca_target_n_components': n_use,\n",
    "        'shape_pca_indices': pca_keep_idx,\n",
    "        'shape_pca_cumvar': achieved_cumvar,\n",
    "        'class_distribution': dict(class_counts),\n",
    "        'class_weights': class_weights,\n",
    "        'selected_base_idx': SELECTED_BASE_IDX,\n",
    "        'feature_names': feature_names,\n",
    "        'X_train': X_all, 'y_train': y_all, 'session_ids': sid_all,\n",
    "    }\n",
