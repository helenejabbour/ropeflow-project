    "        X = (X - np.mean(X, axis=0, keepdims=True)) / (np.std(X, axis=0, keepdims=True) + 1e-6)\n",
    "\n",
    "    if X.ndim < 2 or len(X) == 0:\n",
    "        return {'labels': [], 'confidences': [], 'X': np.empty((0, len(model['feature_names']))),\n",
    "                'verdict': 'unknown', 'vote': {}, 'n_transition': 0, 'session': sess}\n",
    "\n",
    "    X_s = model['scaler'].transform(X)\n",
    "\n",
    "    le = model['label_encoder']\n",
    "    y_pred_enc = model['clf'].predict(X_s)\n",
    "    y_pred_proba = model['clf'].predict_proba(X_s)\n",
    "    pred_labels = le.inverse_transform(y_pred_enc)\n",
    "    confidences = np.max(y_pred_proba, axis=1)\n",
    "\n",
    "    ct = conf_threshold or CONFIG['CONFIDENCE_THRESHOLD']\n",
    "    tt = tmpl_threshold or CONFIG['TEMPLATE_GATE_THRESHOLD']\n",
    "\n",
    "    conf_gate = confidences >= ct\n",
    "    tmpl_gate = np.zeros(len(X), dtype=bool)\n",
    "    for ci in range(len(X)):\n",
    "        cm = build_cycle_matrix(\n",
    "            sess['A0'], sess['A1'], sess['om0'], sess['om1'],\n",
    "            sess['paired0'][ci][0], sess['paired0'][ci][1],\n",
    "            sess['paired1'][ci][0], sess['paired1'][ci][1],\n",
    "            CONFIG['TARGET_LEN'])\n",
    "        for cls_tmpl in model['class_templates'].values():\n",
    "            if template_correlation(cm, cls_tmpl)[0] >= tt:\n",
    "                tmpl_gate[ci] = True\n",
    "                break\n",
    "\n",
    "    labels = [pred_labels[i] if (conf_gate[i] and tmpl_gate[i]) else 'transition'\n",
    "              for i in range(len(X))]\n",
    "\n",
    "    non_trans = [l for l in labels if l != 'transition']\n",
    "    vote = Counter(non_trans) if non_trans else Counter(labels)\n",
    "    verdict = vote.most_common(1)[0][0] if non_trans else 'unknown'\n",
    "\n",
    "    if verbose:\n",
    "        print(f'  {len(labels)} cycles | ', end='')\n",
    "        for cls, cnt in sorted(Counter(labels).items()):\n",
    "            print(f'{cls}={cnt} ', end='')\n",
    "        print(f'-> {verdict}')\n",
    "\n",
    "    return {\n",
    "        'labels': labels, 'confidences': confidences.tolist(), 'X': X,\n",
    "        'verdict': verdict, 'vote': dict(Counter(non_trans)),\n",
    "        'n_transition': sum(1 for l in labels if l == 'transition'),\n",
    "        'session': sess,\n",
    "    }"
