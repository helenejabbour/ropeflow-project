    "def stratified_group_split(sessions, test_fraction=0.2, random_state=42, min_train_fraction=0.8):\n",
    "    \"\"\"\n",
    "    Split sessions into train/test, keeping recording groups atomic.\n",
    "    Stratifies by estimated cycle count (not entry count) to prevent\n",
    "    inverted splits when heterogeneous groups have many entries.\n",
    "    Singletons (classes with only 1 group) always stay in train.\n",
    "    Enforces at least min_train_fraction in train (default: 80%).\n",
    "    \"\"\"\n",
    "    import random as _rand\n",
    "    rng = _rand.Random(random_state)\n",
    "\n",
    "    groups = defaultdict(list)\n",
    "    for e in sessions:\n",
    "        groups[session_group(e)].append(e)\n",
    "\n",
    "    # Estimate cycle count per group (use entry count as proxy if no data loaded)\n",
    "    group_weight = {g: len(entries) for g, entries in groups.items()}\n",
    "\n",
    "    class_to_groups = defaultdict(list)\n",
    "    for gname, entries in groups.items():\n",
    "        for lbl in set(e[2] for e in entries):\n",
    "            class_to_groups[lbl].append(gname)\n",
    "\n",
    "    test_groups = set()\n",
    "    for lbl, gnames in sorted(class_to_groups.items()):\n",
    "        if len(gnames) < 2:\n",
    "            continue  # singleton class — keep in train\n",
    "        # Sort by weight (smallest groups go to test first to avoid overshooting)\n",
    "        gnames_sorted = sorted(gnames, key=lambda g: group_weight[g])\n",
    "        rng.shuffle(gnames_sorted)\n",
    "        # Compute target: test_fraction of total entries for this class\n",
    "        total_entries = sum(len(groups[g]) for g in gnames)\n",
    "        target_test = max(1, total_entries * test_fraction)\n",
    "        if target_test >= total_entries * 0.2:\n",
    "            target_test = 1  # never put more than 1 group in test per class\n",
    "        test_count = 0\n",
    "        for g in gnames_sorted:\n",
    "            if g in test_groups:\n",
    "                test_count += len(groups[g])\n",
    "                continue\n",
    "            if test_count < target_test:  # strict: stop as soon as target met\n",
    "                test_groups.add(g)\n",
    "                test_count += len(groups[g])\n",
    "\n",
    "    # Enforce minimum train fraction (at least 80% by default)\n",
    "    total_entries = len(sessions)\n",
    "    max_test_entries = int(np.floor((1.0 - min_train_fraction) * total_entries))\n",
    "    current_test_entries = sum(len(groups[g]) for g in test_groups)\n",
    "    if current_test_entries > max_test_entries:\n",
    "        # Move smallest test groups back to train until constraint is satisfied\n",
    "        for g in sorted(list(test_groups), key=lambda k: len(groups[k])):\n",
    "            if current_test_entries <= max_test_entries:\n",
    "                break\n",
    "            test_groups.remove(g)\n",
    "            current_test_entries -= len(groups[g])\n",
    "\n",
    "    train = [e for e in sessions if session_group(e) not in test_groups]\n",
    "    test  = [e for e in sessions if session_group(e) in test_groups]\n",
