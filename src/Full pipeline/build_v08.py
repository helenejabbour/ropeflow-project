    "def extract_features(t0, t1, A0, A1, om0, om1,\n",
    "                          paired0, paired1, template, fs,\n",
    "                          shape_pca=None, shape_pca_n_components=None,\n",
    "                          shape_pca_indices=None, selected_base_idx=None):\n",
    "    \"\"\"Extract features with optional dynamic-k shape PCA tail.\"\"\"\n",
    "    features = []\n",
    "    flat_matrices = []\n",
    "    dt = 1.0 / fs\n",
    "    for (s0, e0), (s1, e1) in zip(paired0, paired1):\n",
    "        phys = physics_features(t0, t1, A0, A1, om0, om1, s0, e0, s1, e1, fs)\n",
    "        fourier = cycle_fourier_features(A0, om0, s0, e0, fs)\n",
    "        cm = build_cycle_matrix(A0, A1, om0, om1, s0, e0, s1, e1, CONFIG['TARGET_LEN'])\n",
    "        svd_feat = compute_svd_features(cm, CONFIG['PCA_COMPONENTS'])\n",
    "        corr_mean, corr_min = template_correlation(cm, template)\n",
    "        tmpl_feat = np.array([corr_mean, corr_min])\n",
    "        dmd_feat = dmd_features(cm, dt)\n",
    "        si_feat = subject_invariant_features(t0, t1, A0, A1, om0, om1, s0, e0, s1, e1, fs)\n",
    "        topo = topology_features(om0, om1, s0, e0, s1, e1, fs)\n",
    "        feat = np.concatenate([phys, fourier, svd_feat, tmpl_feat,\n",
    "                               dmd_feat, si_feat, topo])\n",
    "        features.append(feat)\n",
    "        flat_matrices.append(cm.ravel())\n",
    "    X = np.array(features) if features else np.empty((0, len(FULL_BASE_FEAT_NAMES)))\n",
    "    if selected_base_idx is not None:\n",
    "        if len(X) > 0:\n",
    "            X = X[:, selected_base_idx]\n",
    "        else:\n",
    "            X = np.empty((0, len(selected_base_idx)))\n",
    "        pairwise = build_pairwise_features(X)\n",
    "        if len(X) > 0:\n",
    "            X = np.hstack([X, pairwise])\n",
    "        else:\n",
    "            X = np.empty((0, len(BASE_FEAT_NAMES)))\n",
    "    flat_mat = np.array(flat_matrices) if flat_matrices else np.empty((0, 12 * CONFIG['TARGET_LEN']))\n",
    "    if shape_pca is not None and len(flat_mat) > 0:\n",
    "        pca_scores_all = shape_pca.transform(flat_mat)\n",
    "        if shape_pca_indices is not None:\n",
    "            idx = [int(i) for i in shape_pca_indices if int(i) < pca_scores_all.shape[1]]\n",
    "            if not idx:\n",
    "                n_comp = shape_pca.n_components_ if shape_pca_n_components is None else shape_pca_n_components\n",
    "                n_comp = max(1, min(int(n_comp), pca_scores_all.shape[1]))\n",
    "                idx = list(range(n_comp))\n",
    "            pca_scores = pca_scores_all[:, idx]\n",
    "        else:\n",
    "            n_comp = shape_pca.n_components_ if shape_pca_n_components is None else shape_pca_n_components\n",
    "            n_comp = max(1, min(int(n_comp), pca_scores_all.shape[1]))\n",
    "            pca_scores = pca_scores_all[:, :n_comp]\n",
    "        X = np.hstack([X, pca_scores])\n",
    "    return X, flat_mat\n",
