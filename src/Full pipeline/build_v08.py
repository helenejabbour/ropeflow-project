    "# Topology features — capture 3D trajectory shape\n",
    "# ══════════════════════════════════════════════════════════════\n",
    "def topology_features(om0, om1, s0, e0, s1, e1, fs):\n",
    "    \"\"\"Enhanced topology features with sneak-sensitive dynamic descriptors.\"\"\"\n",
    "    feats = []\n",
    "\n",
    "    def _phase_lag_deg(a, b, t_ref):\n",
    "        a_c, b_c = a - np.mean(a), b - np.mean(b)\n",
    "        corr = np.correlate(a_c, b_c, mode='full')\n",
    "        lag_samples = np.argmax(corr) - (len(a) - 1)\n",
    "        phase_deg = (lag_samples / max(1.0, t_ref)) * 360.0\n",
    "        return np.abs((phase_deg + 180.0) % 360.0 - 180.0)\n",
    "\n",
    "    for w_raw in [om0[s0:e0], om1[s1:e1]]:\n",
    "        w = w_raw.copy()\n",
    "        n = len(w)\n",
    "        if n < 6:\n",
    "            feats.extend([0.0] * 19)\n",
    "            continue\n",
    "\n",
    "        wx, wy, wz = w[:, 0], w[:, 1], w[:, 2]\n",
    "\n",
    "        # ---------- 1. Dominant period ----------\n",
    "        w_norm = np.linalg.norm(w, axis=1)\n",
    "        w_norm_c = w_norm - np.mean(w_norm)\n",
    "        acorr = np.correlate(w_norm_c, w_norm_c, mode='full')[n-1:]\n",
    "        min_lag = max(2, int(CONFIG['CYCLE_MIN_PERIOD_S'] * fs))\n",
    "        max_lag = min(n - 2, int(CONFIG['CYCLE_MAX_PERIOD_S'] * fs))\n",
    "        if max_lag > min_lag:\n",
    "            ac_seg = acorr[min_lag:max_lag + 1]\n",
    "            prom = max(1e-12, 0.05 * acorr[0])\n",
    "            peaks, props = find_peaks(ac_seg, prominence=prom)\n",
    "            if len(peaks) > 0:\n",
    "                best = peaks[np.argmax(props['prominences'])]\n",
    "                T_dom = float(min_lag + best)\n",
    "            else:\n",
    "                T_dom = float(max(1, n))\n",
    "        else:\n",
    "            T_dom = float(max(1, n))\n",
    "\n",
    "        # ---------- 2. Cross-axis phase lags ----------\n",
    "        phase_xy = _phase_lag_deg(wx, wy, T_dom)\n",
    "        phase_xz = _phase_lag_deg(wx, wz, T_dom)\n",
    "        phase_yz = _phase_lag_deg(wy, wz, T_dom)\n",
    "        feats.extend([phase_xy, phase_xz, phase_yz])\n",
    "\n",
    "        # ---------- 3. SVD spatial ratios ----------\n",
    "        _, S_w, _ = np.linalg.svd(w.T, full_matrices=False)\n",
    "        if len(S_w) >= 3:\n",
    "            s_sum = S_w[0] + 1e-12\n",
    "            feats.append(S_w[1] / s_sum)  # planarity\n",
    "            feats.append(S_w[2] / s_sum)  # spatial complexity\n",
    "        else:\n",
    "            feats.extend([0.0, 0.0])\n",
    "\n",
    "        # ---------- 4. Winding numbers ----------\n",
    "        cycles = max(1.0, n / T_dom)\n",
    "        wx_zc = np.sum(np.diff(np.sign(wx - np.mean(wx))) != 0)\n",
    "        wy_zc = np.sum(np.diff(np.sign(wy - np.mean(wy))) != 0)\n",
    "        feats.extend([wx_zc / cycles, wy_zc / cycles])\n",
    "\n",
    "        # ---------- 5. Axis energy distribution ----------\n",
    "        energy = np.array([np.mean(wx**2), np.mean(wy**2), np.mean(wz**2)])\n",
    "        energy /= (np.sum(energy) + 1e-12)\n",
    "        feats.extend(energy.tolist())\n",
    "\n",
    "        # ---------- 6. Torsion + variability ----------\n",
    "        v_shift = np.roll(w, -1, axis=0)\n",
    "        cross_mag = np.linalg.norm(np.cross(w[:-1], v_shift[:-1]), axis=1)\n",
    "        torsion = float(np.mean(cross_mag)) if len(cross_mag) else 0.0\n",
    "        torsion_var = float(np.var(cross_mag)) if len(cross_mag) > 1 else 0.0\n",
    "        feats.extend([torsion, torsion_var])\n",
    "\n",
    "        # ---------- 7. Temporal variability ----------\n",
    "        temporal_var = float(np.var(w_norm))\n",
    "\n",
    "        # Phase variability in short windows (sneak sensitivity)\n",
    "        phase_win = max(6, int(0.5 * T_dom))\n",
    "        phase_step = max(1, phase_win // 2)\n",
    "        phase_xy_vals, phase_yz_vals = [], []\n",
    "        if phase_win < n:\n",
    "            for st in range(0, n - phase_win + 1, phase_step):\n",
    "                swx = wx[st:st + phase_win]\n",
    "                swy = wy[st:st + phase_win]\n",
    "                swz = wz[st:st + phase_win]\n",
    "                phase_xy_vals.append(_phase_lag_deg(swx, swy, T_dom))\n",
    "                phase_yz_vals.append(_phase_lag_deg(swy, swz, T_dom))\n",
    "        phase_xy_var = float(np.var(phase_xy_vals)) if len(phase_xy_vals) > 1 else 0.0\n",
    "        phase_yz_var = float(np.var(phase_yz_vals)) if len(phase_yz_vals) > 1 else 0.0\n",
    "\n",
    "        # Local nonstationarity: variance of short-window temporal variance\n",
    "        local_win = max(6, int(0.25 * T_dom))\n",
    "        local_step = max(1, local_win // 2)\n",
    "        local_vars = []\n",
    "        if local_win < n:\n",
    "            for st in range(0, n - local_win + 1, local_step):\n",
    "                local_vars.append(np.var(w_norm[st:st + local_win]))\n",
    "        local_temporal_var = float(np.var(local_vars)) if len(local_vars) > 1 else 0.0\n",
    "        rolling_var_mean = float(np.mean(local_vars)) if len(local_vars) > 0 else 0.0\n",
    "        temporal_var_deriv = float(np.mean(np.abs(np.diff(local_vars)))) if len(local_vars) > 1 else 0.0\n",
    "        phase_xy_drift = float(np.mean(np.abs(np.diff(phase_xy_vals)))) if len(phase_xy_vals) > 1 else 0.0\n",
    "        phase_yz_drift = float(np.mean(np.abs(np.diff(phase_yz_vals)))) if len(phase_yz_vals) > 1 else 0.0\n",
    "        phase_drift = 0.5 * (phase_xy_drift + phase_yz_drift)\n",
    "\n",
    "        feats.extend([\n",
    "            temporal_var, phase_xy_var, phase_yz_var, local_temporal_var,\n",
    "            rolling_var_mean, temporal_var_deriv, phase_drift\n",
    "        ])\n",
    "\n",
    "    return np.array(feats)\n",
    "\n",
