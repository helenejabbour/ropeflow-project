    "def build_pairwise_features(X_base_selected, eps=PAIRWISE_EPS):\n",
    "    \"\"\"Targeted pairwise features for known weak class boundaries.\"\"\"\n",
    "    if X_base_selected.ndim != 2 or X_base_selected.shape[0] == 0:\n",
    "        return np.empty((0, len(PAIRWISE_FEATURE_NAMES)))\n",
    "    idx = {n: i for i, n in enumerate(SELECTED_BASE_FEATURES)}\n",
    "    wy = X_base_selected[:, idx['wind_wy_d0']]\n",
    "    wx = X_base_selected[:, idx['wind_wx_d0']]\n",
    "    phase_xz = X_base_selected[:, idx['phase_xz_d0']]\n",
    "    phase_xy = X_base_selected[:, idx['phase_xy_d0']]\n",
    "    spatial = X_base_selected[:, idx['spatial_ratio_3_1_d0']]\n",
    "    planar = X_base_selected[:, idx['plane_ratio_2_1_d0']]\n",
    "    local_tvar = X_base_selected[:, idx['local_temporal_var_d0']]\n",
    "    tvar = X_base_selected[:, idx['temporal_var_d0']]\n",
    "    phase_yz_d0 = X_base_selected[:, idx['phase_yz_d0']]\n",
    "    phase_yz_d1 = X_base_selected[:, idx['phase_yz_d1']]\n",
    "    torsion_var = X_base_selected[:, idx['torsion_var_d0']]\n",
    "    torsion = X_base_selected[:, idx['torsion_d0']]\n",
    "    return np.column_stack([\n",
    "        wy / (wx + eps),\n",
    "        np.abs(phase_xz - phase_xy),\n",
    "        spatial - planar,\n",
    "        local_tvar / (tvar + eps),\n",
    "        np.abs(phase_yz_d0 - phase_yz_d1),\n",
    "        torsion_var / (np.abs(torsion) + eps),\n",
    "    ])\n",
